# -----------------------------------------------------------------------------
# Solution
# -----------------------------------------------------------------------------

# print() is a built-in Python function that outputs the given value(s) to
# stdout followed by a newline character (\n) by default.
# The argument "Hello, World!" is a string literal — a fixed sequence of
# characters enclosed in double quotes.
print("Hello, World!")


# -----------------------------------------------------------------------------
# Alternative Approach (using a variable)
# -----------------------------------------------------------------------------

# You can also store a string in a variable first, then print it.
# A variable is a named container that holds a value in memory.
# Here, `message` holds the string "Hello, World!".

# Uncomment the lines below to try this approach:
# message = "Hello, World!"
# print(message)

# Both approaches produce identical output. Using a variable becomes useful
# when the same value needs to be reused multiple times in a program.


